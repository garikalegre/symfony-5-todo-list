<?php

namespace App\Tasks\Application\Command;

use App\Authentication\Domain\User\Entity\User;
use App\Common\Application\CQRS\Command\Command;
use DateTime;

class CreateTaskCommand implements Command
{
    private User $user;
    private string $name;
    private DateTime $date;
    private string $status;

    /**
     * @return User
     */
    public function getUser(): User
    {
        return $this->user;
    }

    /**
     * @param User $user
     */
    public function setUser(User $user): void
    {
        $this->user = $user;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }

    /**
     * @return DateTime
     */
    public function getDate(): DateTime
    {
        return $this->date;
    }

    /**
     * @param string $date
     */
    public function setDate(string $date): void
    {
        $this->date = DateTime::createFromFormat('Y-m-d', $date)->setTime(0,0);
    }

    /**
     * @return string
     */
    public function getStatus(): string
    {
        return $this->status;
    }

    /**
     * @param string $status
     */
    public function setStatus(string $status): void
    {
        $this->status = $status;
    }
}
